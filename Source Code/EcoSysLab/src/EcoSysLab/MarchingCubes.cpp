void MarchingCubes::TriangulateCell(MarchingCubeCell& cell, float isovalue, std::vector<Vertex>& vertices)
{
	int cubeIndex = 0;
	for (int i = 0; i < 8; i++)
		if (cell.m_value[i] < isovalue) cubeIndex |= (1 << i);
	std::vector<glm::vec3> intersections(12);
	int intersectionsKey = m_edgeTable[cubeIndex];
	int idx = 0;
	while (intersectionsKey)
	{
		if (intersectionsKey & 1)
		{
			int v1 = m_edgeToVertices[idx].first, v2 = m_edgeToVertices[idx].second;
			float mu = (isovalue - cell.m_value[v1]) / (cell.m_value[v2] - cell.m_value[v1]);
			const glm::vec3 intersectionPoint = { mu * (cell.m_vertex[v2].x - cell.m_vertex[v1].x) + cell.m_vertex[v1].x,
				mu * (cell.m_vertex[v2].y - cell.m_vertex[v1].y) + cell.m_vertex[v1].y,
			mu * (cell.m_vertex[v2].z - cell.m_vertex[v1].z) + cell.m_vertex[v1].z };
			intersections[idx] = intersectionPoint;
		}
		idx++;
		intersectionsKey >>= 1;
	}
	Vertex archetype;
	for (int i = 0; m_triangleTable[cubeIndex][i] != -1; i += 3)
	{
		for (int j = 2; j >= 0; j--) {

			archetype.m_position = intersections[m_triangleTable[cubeIndex][i + j]];
			vertices.push_back(archetype);
		}
	}
}

